// помигать светом заданное число раз
void flash_led (uint8_t number) {
	for (uint8_t i=0; i<number; i++) {
		HAL_GPIO_WritePin (LED_GPIO_Port, LED_Pin, 1);
		HAL_Delay(500);
		HAL_GPIO_WritePin (LED_GPIO_Port, LED_Pin, 0);
		HAL_Delay(100);
	}

}





